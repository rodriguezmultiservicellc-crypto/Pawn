-- BEFORE INSERT trigger to assign SKU when not provided.
CREATE OR REPLACE FUNCTION inventory_items_assign_sku()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.sku IS NULL OR NEW.sku = '' THEN
    NEW.sku_number := next_tenant_counter(NEW.tenant_id, 'inventory_sku');
    NEW.sku := 'INV-' || LPAD(NEW.sku_number::TEXT, 6, '0');
  ELSIF NEW.sku_number IS NULL THEN
    -- App-provided SKU: stash the counter value anyway so sortable order
    -- still works. Don't advance the counter (user-provided SKUs are
    -- expected to live outside the auto-numbered range).
    NEW.sku_number := 0;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_inventory_items_sku ON inventory_items;
CREATE TRIGGER trg_inventory_items_sku
BEFORE INSERT ON inventory_items
FOR EACH ROW EXECUTE FUNCTION inventory_items_assign_sku();

