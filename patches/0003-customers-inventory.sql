-- ───────────────────────────────────────────────────────────────────────────
--  INVENTORY TRANSFERS — move items between sibling shops in a chain.
-- ───────────────────────────────────────────────────────────────────────────
-- Cross-chain transfers are blocked at the trigger level. Standalone tenants
-- (no parent_tenant_id) cannot use transfers at all.

CREATE TABLE IF NOT EXISTS inventory_transfers (
  id              UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  -- For RLS: every transfer belongs to its origin tenant. The destination
  -- tenant gets read access via a separate policy (its own siblings list).
  tenant_id       UUID NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,

  from_tenant_id  UUID NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
  to_tenant_id    UUID NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
  item_id         UUID NOT NULL REFERENCES inventory_items(id) ON DELETE RESTRICT,

  status          transfer_status NOT NULL DEFAULT 'pending',
  shipped_at      TIMESTAMPTZ,
  received_at     TIMESTAMPTZ,
  cancelled_at    TIMESTAMPTZ,

  carrier         TEXT,
  tracking_number TEXT,
  notes           TEXT,

  created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  created_by      UUID REFERENCES auth.users(id),
  updated_by      UUID REFERENCES auth.users(id),

  CONSTRAINT inventory_transfers_distinct_tenants CHECK (from_tenant_id <> to_tenant_id),
  CONSTRAINT inventory_transfers_origin_match    CHECK (tenant_id = from_tenant_id)
);

CREATE INDEX IF NOT EXISTS idx_inventory_transfers_from ON inventory_transfers(from_tenant_id, status, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_inventory_transfers_to   ON inventory_transfers(to_tenant_id,   status, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_inventory_transfers_item ON inventory_transfers(item_id);

-- Block cross-chain transfers: from and to MUST share the same parent_tenant_id,
-- and that parent must be non-null (i.e., both are children of a chain_hq).
CREATE OR REPLACE FUNCTION inventory_transfers_validate_siblings()
RETURNS TRIGGER AS $$
DECLARE
  v_from_parent UUID;
  v_to_parent   UUID;
BEGIN
  SELECT parent_tenant_id INTO v_from_parent FROM tenants WHERE id = NEW.from_tenant_id;
  SELECT parent_tenant_id INTO v_to_parent   FROM tenants WHERE id = NEW.to_tenant_id;

  IF v_from_parent IS NULL OR v_to_parent IS NULL THEN
    RAISE EXCEPTION 'inventory_transfers requires both shops to belong to a chain (parent_tenant_id NOT NULL).';
  END IF;
  IF v_from_parent <> v_to_parent THEN
    RAISE EXCEPTION 'inventory_transfers blocked: from_tenant and to_tenant must share the same parent_tenant_id (same chain).';
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_inventory_transfers_siblings ON inventory_transfers;
CREATE TRIGGER trg_inventory_transfers_siblings
BEFORE INSERT OR UPDATE OF from_tenant_id, to_tenant_id ON inventory_transfers
FOR EACH ROW EXECUTE FUNCTION inventory_transfers_validate_siblings();

