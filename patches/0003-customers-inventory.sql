-- Ensure exactly one primary photo per item: trigger demotes others.
CREATE OR REPLACE FUNCTION inventory_photos_one_primary()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.is_primary = TRUE THEN
    UPDATE inventory_item_photos
       SET is_primary = FALSE
     WHERE item_id = NEW.item_id
       AND id <> NEW.id
       AND is_primary = TRUE;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_inventory_photos_one_primary ON inventory_item_photos;
CREATE TRIGGER trg_inventory_photos_one_primary
AFTER INSERT OR UPDATE OF is_primary ON inventory_item_photos
FOR EACH ROW WHEN (NEW.is_primary = TRUE)
EXECUTE FUNCTION inventory_photos_one_primary();

