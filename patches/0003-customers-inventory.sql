-- ───────────────────────────────────────────────────────────────────────────
--  TENANT COUNTERS — generic per-tenant monotonic sequence.
-- ───────────────────────────────────────────────────────────────────────────
-- Used by inventory SKUs in this migration; pawn ticket numbers, sale
-- receipt numbers, repair ticket numbers will reuse it in later phases.
-- Each (tenant_id, counter_name) advances independently.

CREATE TABLE IF NOT EXISTS tenant_counters (
  tenant_id     UUID NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  counter_name  TEXT NOT NULL,
  last_value    BIGINT NOT NULL DEFAULT 0,
  updated_at    TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  PRIMARY KEY (tenant_id, counter_name)
);

CREATE OR REPLACE FUNCTION next_tenant_counter(
  p_tenant_id     UUID,
  p_counter_name  TEXT
) RETURNS BIGINT
LANGUAGE plpgsql
AS $$
#variable_conflict use_column
DECLARE v_value BIGINT;
BEGIN
  INSERT INTO tenant_counters (tenant_id, counter_name, last_value)
  VALUES (p_tenant_id, p_counter_name, 1)
  ON CONFLICT (tenant_id, counter_name) DO UPDATE
    SET last_value = tenant_counters.last_value + 1,
        updated_at = NOW()
  RETURNING last_value INTO v_value;
  RETURN v_value;
END;
$$;

